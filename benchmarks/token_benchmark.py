#!/usr/bin/env python3
"""
benchmarks/token_benchmark.py — Empirical token measurement using tiktoken.

Measures actual token counts for two AI code review scenarios:
  A) Without mustel: AI must discover + diagnose + fix all issues
  B) With mustel:    AI receives agent_prompt, skips diagnosis, jumps to fixes

Uses OpenAI's tiktoken library for deterministic, reproducible token counts.
Encoding: cl100k_base (GPT-4 / GPT-4o family)
          o200k_base  (GPT-4o-mini / latest models)

This is NOT a model — these are exact token counts from the same tokenizer
the OpenAI API uses for billing. The methodology is reproducible by anyone
with `pip install tiktoken`.
"""

from __future__ import annotations

import os
import json
import tiktoken

# ─────────────────────────────────────────────
#  Paths
# ─────────────────────────────────────────────

BENCHMARK_DIR = os.path.dirname(os.path.abspath(__file__))
PROJECT_DIR = os.path.join(BENCHMARK_DIR, "projects", "project_backend")
TOKEN_DATA_DIR = os.path.join(BENCHMARK_DIR, "token_data")
APP_FILE = os.path.join(PROJECT_DIR, "app.py")

RESPONSE_WITHOUT = os.path.join(TOKEN_DATA_DIR, "response_without_mustel.txt")
RESPONSE_WITH = os.path.join(TOKEN_DATA_DIR, "response_with_mustel.txt")

# The actual agent_prompt from mustel's latest run on project_backend
MUSTEL_AGENT_PROMPT = (
    "mustel found 20 issues: HighSec:S001,S002,S003,S004,S006 | "
    "Errs:E001,E002,E003 | MedSec:S005 | "
    "Warns:W001,W002,W003,W004,W005,W006,W007,W008,W009,W010. "
    "Use IDs to lookup details in JSON."
)


def count_tokens(text: str, encoding_name: str = "cl100k_base") -> int:
    """Count tokens using tiktoken. Deterministic and reproducible."""
    enc = tiktoken.get_encoding(encoding_name)
    return len(enc.encode(text))


def load_file(path: str) -> str:
    with open(path, "r", encoding="utf-8") as f:
        return f.read()


def run_token_benchmark():
    """Run the full token measurement and print results."""

    # Load source code
    app_source = load_file(APP_FILE)

    # Load AI responses (generated by a real AI, not modeled)
    response_without = load_file(RESPONSE_WITHOUT)
    response_with = load_file(RESPONSE_WITH)

    # ─────────────────────────────────────────
    #  Scenario A: Without mustel
    # ─────────────────────────────────────────
    prompt_a = (
        "Review the following Python application for bugs and security issues. "
        "List every issue you find with its line number, severity, and a code fix.\n\n"
        f"File: project_backend/app.py\n\n```python\n{app_source}\n```"
    )

    # ─────────────────────────────────────────
    #  Scenario B: With mustel context
    # ─────────────────────────────────────────
    prompt_b = (
        "The following mustel static analysis report was generated for this project. "
        "Apply fixes for all high-security issues identified.\n\n"
        f"MUSTEL REPORT:\n{MUSTEL_AGENT_PROMPT}\n\n"
        f"File: project_backend/app.py\n\n```python\n{app_source}\n```"
    )

    # ─────────────────────────────────────────
    #  Count tokens (two encodings for robustness)
    # ─────────────────────────────────────────
    encodings = ["cl100k_base", "o200k_base"]

    print("=" * 65)
    print("  MUSTEL TOKEN BENCHMARK - Empirical Measurement via tiktoken")
    print("=" * 65)

    results = {}

    for enc_name in encodings:
        input_a = count_tokens(prompt_a, enc_name)
        input_b = count_tokens(prompt_b, enc_name)
        output_a = count_tokens(response_without, enc_name)
        output_b = count_tokens(response_with, enc_name)

        total_a = input_a + output_a
        total_b = input_b + output_b
        delta = total_a - total_b
        savings_pct = (delta / total_a) * 100 if total_a > 0 else 0

        results[enc_name] = {
            "input_tokens_without_mustel": input_a,
            "input_tokens_with_mustel": input_b,
            "output_tokens_without_mustel": output_a,
            "output_tokens_with_mustel": output_b,
            "total_without_mustel": total_a,
            "total_with_mustel": total_b,
            "token_delta": delta,
            "savings_percent": round(savings_pct, 1),
        }

        print(f"\n  Encoding: {enc_name}")
        print(f"  {'-'*60}")
        print(f"  {'Metric':<40} {'Without':>10} {'With':>10}")
        print(f"  {'-'*60}")
        print(f"  {'Input tokens (prompt + code)':<40} {input_a:>10,} {input_b:>10,}")
        print(f"  {'Output tokens (AI response)':<40} {output_a:>10,} {output_b:>10,}")
        print(f"  {'-'*60}")
        print(f"  {'TOTAL tokens':<40} {total_a:>10,} {total_b:>10,}")
        print(f"  {'Token delta':<40} {delta:>10,}")
        print(f"  {'Savings':<40} {savings_pct:>9.1f}%")

    # ─────────────────────────────────────────
    #  Where the savings come from
    # ─────────────────────────────────────────
    print(f"\n{'=' * 65}")
    print("  WHERE THE SAVINGS COME FROM")
    print(f"{'=' * 65}")

    enc = "cl100k_base"
    r = results[enc]
    input_delta = r["input_tokens_without_mustel"] - r["input_tokens_with_mustel"]
    output_delta = r["output_tokens_without_mustel"] - r["output_tokens_with_mustel"]

    print(f"\n  Using {enc}:")
    print(f"  Input token change:  {input_delta:+d} tokens "
          f"({'saved' if input_delta > 0 else 'added (mustel prompt overhead)'})")
    print(f"  Output token change: {output_delta:+d} tokens "
          f"({'saved' if output_delta > 0 else 'added'})")
    print(f"\n  Input cost note:  mustel adds ~{abs(input_delta) if input_delta < 0 else 0} input tokens "
          f"(agent_prompt overhead)")
    print(f"  Output cost note: mustel saves ~{output_delta} output tokens "
          f"(AI skips diagnosis, goes to fixes)")
    print(f"\n  Output tokens are 3-4x more expensive than input tokens on most APIs.")
    print(f"  So the output savings dominate the total cost reduction.")

    # ─────────────────────────────────────────
    #  Supplementary: raw character counts
    # ─────────────────────────────────────────
    print(f"\n{'=' * 65}")
    print("  RAW CHARACTER COUNTS")
    print(f"{'=' * 65}")
    print(f"  app.py source:              {len(app_source):>6,} chars")
    print(f"  agent_prompt:               {len(MUSTEL_AGENT_PROMPT):>6,} chars")
    print(f"  Prompt A (without mustel):  {len(prompt_a):>6,} chars")
    print(f"  Prompt B (with mustel):     {len(prompt_b):>6,} chars")
    print(f"  Response A (without):       {len(response_without):>6,} chars")
    print(f"  Response B (with):          {len(response_with):>6,} chars")

    # ─────────────────────────────────────────
    #  Save results
    # ─────────────────────────────────────────
    output_file = os.path.join(TOKEN_DATA_DIR, "token_results.json")
    with open(output_file, "w") as f:
        json.dump({
            "methodology": "tiktoken deterministic token counting",
            "note": "AI responses generated by a real AI (Claude), not modeled",
            "source_file": "project_backend/app.py",
            "source_lines": len(app_source.splitlines()),
            "agent_prompt_chars": len(MUSTEL_AGENT_PROMPT),
            "results": results,
        }, f, indent=2)

    print(f"\n  Results saved to: {output_file}")
    print("=" * 65)


if __name__ == "__main__":
    run_token_benchmark()
